/**
 * Generates scripts/dashoguz_official.txt (366 rows) and
 *          src/namazym/services/prayer/dataset.ts
 *
 * Source: global_vakitler_v3.json в†’ dasoguz region, 2026
 * Feb 29: interpolated from Feb 28 + Mar 1 average
 */
const fs   = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const v3   = JSON.parse(
    fs.readFileSync(path.join(ROOT, 'src/namazym/data/global_vakitler_v3.json'), 'utf8'),
);

// Find Dashoguz region
const regions2026 = v3.years['2026'].regions;
const regionKey   = Object.keys(regions2026).find(k => k.toLowerCase().includes('das'));
if (!regionKey) { console.error('Dashoguz region not found'); process.exit(1); }
const rawDays = regions2026[regionKey].days;

// в”Ђв”Ђв”Ђ Helpers в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
const NUM_TO_MONTH = {
    '01':'Гќanwar','02':'Fewral','03':'Mart','04':'Aprel','05':'MaГЅ','06':'IГЅun',
    '07':'IГЅul','08':'Awgust','09':'SentГЅabr','10':'OktГЅabr','11':'NoГЅabr','12':'Dekabr',
};

function toMins(t) { const [h, m] = t.split(':').map(Number); return h * 60 + m; }
function fromMins(total) {
    const h = Math.floor(total / 60);
    const m = total % 60;
    return `${h}:${String(m).padStart(2, '0')}`;
}

// в”Ђв”Ђв”Ђ Build MM-DD keyed map в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
const mmddMap = {};
for (const d of rawDays) {
    const mmdd = d.dt.substring(5); // "2026-03-15" в†’ "03-15"
    mmddMap[mmdd] = { f: d.f, e: d.e, o: d.o, i: d.i, a: d.a, y: d.y };
}

// в”Ђв”Ђв”Ђ Interpolate Feb 29 в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
const feb28 = mmddMap['02-28'];
const mar01  = mmddMap['03-01'];
if (!feb28 || !mar01) { console.error('Feb 28 or Mar 1 missing'); process.exit(1); }

const feb29 = {};
for (const k of ['f', 'e', 'o', 'i', 'a', 'y']) {
    feb29[k] = fromMins(Math.round((toMins(feb28[k]) + toMins(mar01[k])) / 2));
}
mmddMap['02-29'] = feb29;
console.log(`Feb 29 interpolated: fajr=${feb29.f} sunrise=${feb29.e} dhuhr=${feb29.o} asr=${feb29.i} maghrib=${feb29.a} isha=${feb29.y}`);

// в”Ђв”Ђв”Ђ Sort all 366 keys в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
const allKeys = Object.keys(mmddMap).sort();
if (allKeys.length !== 366) {
    console.error(`Expected 366 days, got ${allKeys.length}`); process.exit(1);
}

// в”Ђв”Ђв”Ђ Build raw text в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
const HEADER = 'AГЅ;GГјn;Agyz beklenГЅГ¤n wagty;Ertir namazy;GГјnГјЕ€ dogГЅan wagty;Г–ГЅle namazy;Ikindi namazy;AgЕџam namazy;Гќassy namazy';
const dataLines = [];

for (const mmdd of allKeys) {
    const [mm, dd]  = mmdd.split('-');
    const monthName = NUM_TO_MONTH[mm];
    const dayNum    = String(parseInt(dd, 10));
    const d         = mmddMap[mmdd];
    // Agyz в‰€ fajr - 40 min (ignored by parser; only for format compliance)
    const agyzMins  = Math.max(0, toMins(d.f) - 40);
    const agyz      = fromMins(agyzMins);

    dataLines.push(`${monthName};${dayNum};${agyz};${d.f};${d.e};${d.o};${d.i};${d.a};${d.y}`);
}

const rawText = [HEADER, ...dataLines].join('\n');

// в”Ђв”Ђв”Ђ Write raw source file в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
const rawOut = path.join(ROOT, 'data_sources/dashoguz_official.txt');
fs.writeFileSync(rawOut, rawText, 'utf8');
console.log(`вњ… Written raw text (${dataLines.length} rows): ${rawOut}`);

// в”Ђв”Ђв”Ђ Generate dataset.ts в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
// Escape backticks in raw text (none expected, but be safe)
const escapedRaw = rawText.replace(/`/g, '\\`').replace(/\$\{/g, '\\${');

const datasetTs = `/**
 * Dashoguz Official Prayer Timetable вЂ” 366-day base dataset
 *
 * Generated from global_vakitler_v3.json (dasoguz region, 2026).
 * Feb 29 is interpolated from Feb 28 / Mar 01 average.
 * Replace with official Feb 29 value when the Muftiyat publishes a leap-year schedule.
 *
 * Key format: "MM-DD" вЂ” year-agnostic, works for every year.
 * Non-leap years never query "02-29"; the engine handles this.
 *
 * DO NOT EDIT this file manually вЂ” regenerate via scripts/generate_dataset_ts.js
 */

import { parseRaw } from './parser';

const RAW = \`${escapedRaw}\`;

export const DATASET = parseRaw(RAW);
`;

const tsOut = path.join(ROOT, 'src/namazym/services/prayer/dataset.ts');
fs.writeFileSync(tsOut, datasetTs, 'utf8');
console.log(`вњ… Written dataset.ts: ${tsOut}`);

// в”Ђв”Ђв”Ђ Quick sanity checks в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
console.log('\nSanity checks on generated data:');
const sampleKeys = ['01-01', '06-15', '12-31', '02-28', '02-29', '03-01'];
for (const k of sampleKeys) {
    const d = mmddMap[k];
    if (!d) { console.log(`  ${k}: вќЊ MISSING`); continue; }
    console.log(`  ${k}: fajr=${d.f} sunrise=${d.e} dhuhr=${d.o} asr=${d.i} maghrib=${d.a} isha=${d.y}`);
}
console.log('\nTotal rows:', allKeys.length, allKeys.length === 366 ? 'вњ…' : 'вќЊ');
